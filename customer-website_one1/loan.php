              <li>Time to maturity. Time to maturity describes the length of the loan contract. Loans are classified according to their maturity into short-term debt, intermediate-term debt, and long-term debt. Revolving credit and perpetual debt have no fixed date for retirement. Banks provide revolving credit through extension of a line of credit. Brokerage firms supply margin credit for qualified customers on certain securities. In these cases, the borrower constantly turns over the line of credit by paying it down and reborrowing the funds when needed. A perpetual loan requires only regular interest payments. The borrower, who usually issued such debt through a registered offering, determines the timing of the debt retirement.</li>
