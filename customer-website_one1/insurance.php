          <div class="col-md-12">
            <h3 class="head_h2">Characteristics of Insurance</h3>
            <h5 class="insurance-h5">Sharing of Risk</h5>
            <p class="insurance-p-text">
              Insurance is a device to share the financial losses which might befall on an individual or his family on the happening of a specified event. The event may be death of a bread-winner to the family in the case of life insurance, marine-perils in marine insurance, fire in fire insurance and other certain events in general insurance, e.g., theft in burglary insurance, accident in motor insurance, etc. The loss arising nom these events if insured are shared by all the insured in the form of premium.
            </p> 
            <h5 class="insurance-h5">Co-operative Device</h5>
            <p class="insurance-p-text">The most important feature of every insurance plan is the co-operation of large number of persons who, in effect, agree to share the financial loss arising due to a particular risk which is insured. Such a group of persons may be brought together voluntarily or through publicity or through solicitation of the agents.</p>
            <h5 class="insurance-h5">Value of Risk</h5>
            <p class="insurance-p-text">The risk is evaluated before insuring to charge the amount of share of an insured, herein called, consideration or premium. There are several methods of evaluation of risks. If there is expectation of more loss, higher premium may be charged. So, the probability of loss is calculated at the time of insurance.</p>
            <h5 class="insurance-h5">Payment at Contingency</h5>
            <p class="insurance-p-text">The payment is made at a certain contingency insured. If the contingency occurs, payment is made. Since the life insurance contract is a contract of certainty, because the contingency, the death or the expiry of term, will certainly occur, the payment is certain. In other insurance contracts, the contingency is the fire or the marine perils etc., may or may not occur. So, if the contingency occurs, payment is made, otherwise no amount is given to the policy-holder.</p>
            <h5 class="insurance-h5">Amount of Payment</h5>
            <p class="insurance-p-text">The amount of payment depends upon the value of loss occurred due to the particular insured risk provided insurance is there up to that amount. In life insurance, the purpose is not to make good the financial loss suffered. The insurer promises to pay a fixed sum on the happening of an event.</p>
            <h5 class="insurance-h5">Large Number of Insured Persons</h5>
            <p class="insurance-p-text">To make the insurance cheaper, it is essential to insure large number of persons or property because the lesser would be cost of insurance and so, the lower would be premium. In past years, tariff associations or mutual fire insurance associations were found to share the loss at cheaper rate. In order to function successfully, the insurance should be joined by a large number of persons.</p>
            <h5 class="insurance-h5">Insurance is not a gambling</h5>
            <p class="insurance-p-text">The insurance serves indirectly to increase the productivity of the community by eliminating worry and increasing initiative. The uncertainty is changed into certainty by insuring property and life because the insurer promises to pay a definite sum at damage or death.</p>
            <h5 class="insurance-h5">Insurance is not Charity</h5>
            <p class="insurance-p-text">Charity is given without consideration but insurance is not possible without premium. It provides security and safety to an individual and to the society although it is a kind of business because in consideration of premium it guarantees the payment of loss. It is a profession because it provides adequate sources at the time of disasters only by charging a nominal premium for the service.</p>
